#Perintah print() didalam python dapat dikombinasikan menggunakan karakter +. Contoh
a = "Hadi Nurhidayat"
print("Nama saya adalah " + a )

#Selain itu juga karakter + dapat menambahkan variabel ke variabel lain. Misal :
a = "Hadi"
b = "Nurhidayat"
print("Nama saya adalah" + " " + a + " " + b)

#Selain menambahkan variabel dengan tipe data string. Karakter + juga dapat digunakan untuk operasi Matematika
a = 1
b = 2
print(a+b)