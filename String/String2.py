#Menampilkan Karakter Dari Posisi 2 ke Posisi 5. Selain Posisi itu tidak dapat ditampilkan
print("1. Menampilkan Karakter Dari Posisi 2 ke Posisi 5")
a = "Hello World"
print(a)
print(a[2:5])
print("==================================================")
#Menampilkan Karakter Dari Posisi Awal Sampai Posisi 5
print("2. Menampilkan Karakter Dari Posisi Awal ke Posisi 5")
a = "Hello World"
print(a)
print(a[:5])
print("==================================================")

#Menampilkan Karakter Dari Posisi 2 Sampai Akhir
print("3. Menampilkan Karakter Dari Posisi 2 Sampai Akhir")
a = "Hello World"
print(a)
print(a[2:])
print("==================================================")

#Menampilkan Karakter Menggunakan Posisi Negatif
print("4. Menampilkan Karakter Menggunakan Posisi Negatif -5 ke -2")
a = "Hello World"
print(a)
print(a[-5:-2])
print("==================================================")

